/**
 * Regenerate registry.generated.ts with the full ~50-nutrient per-100g panel
 * extracted from USDA FDC SR Legacy (.cache/fdc/panels.json, keyed by fdcId).
 * Preserves all ingredient metadata; only widens per100g. Real values only —
 * a nutrient genuinely absent from the FDC record falls back to the prior value
 * or 0.
 *
 *   node scripts/nutrition/expand-registry.mjs   (run via tsx)
 */
import fs from "node:fs";

const base = new URL("../../", import.meta.url);
const reg = await import(
  new URL("src/data/ingredients/registry.generated.ts", base)
);
const INGREDIENTS = reg.INGREDIENTS;
const panels = JSON.parse(
  fs.readFileSync(new URL(".cache/fdc/panels.json", base), "utf8"),
);

// Canonical per-100g key order (grouped).
const KEYS = [
  "calories",
  "water_g",
  "alcohol_g",
  "caffeine_mg",
  "protein_g",
  "totalFat_g",
  "totalCarbs_g",
  "fiber_g",
  "totalSugars_g",
  "addedSugar_g",
  "starch_g",
  "saturatedFat_g",
  "monoFat_g",
  "polyFat_g",
  "transFat_g",
  "omega3_g",
  "cholesterol_mg",
  "vitaminA_mcg_rae",
  "vitaminC_mg",
  "vitaminD_mcg",
  "vitaminE_mg",
  "vitaminK_mcg",
  "thiamin_mg",
  "riboflavin_mg",
  "niacin_mg",
  "pantothenicAcid_mg",
  "vitaminB6_mg",
  "folate_mcg",
  "vitaminB12_mcg",
  "choline_mg",
  "calcium_mg",
  "iron_mg",
  "magnesium_mg",
  "phosphorus_mg",
  "potassium_mg",
  "sodium_mg",
  "zinc_mg",
  "copper_mg",
  "manganese_mg",
  "selenium_mcg",
  "histidine_g",
  "isoleucine_g",
  "leucine_g",
  "lysine_g",
  "methionine_g",
  "phenylalanine_g",
  "threonine_g",
  "tryptophan_g",
  "valine_g",
  "cystine_g",
  "tyrosine_g",
];

const r3 = (v) => Math.round(v * 1000) / 1000;

let added = 0;
const out = {};
for (const [id, ing] of Object.entries(INGREDIENTS)) {
  const panel = panels[ing.fdcId] || {};
  const per100g = {};
  for (const k of KEYS) {
    const v = panel[k] !== undefined ? panel[k] : (ing.per100g?.[k] ?? 0);
    per100g[k] = r3(v);
  }
  added += KEYS.filter((k) => ing.per100g?.[k] === undefined).length;
  out[id] = { ...ing, per100g };
}

const header = `/**
 * GENERATED by scripts/nutrition/expand-registry.mjs — DO NOT EDIT BY HAND.
 *
 * Canonical ingredient registry. Per-100g nutrient vectors are real USDA
 * FoodData Central SR Legacy values (public domain, ~50-nutrient panel incl.
 * all B vitamins, C/D/E/K, the full mineral set, and the essential amino
 * acids); food-identity facts are curated in the ingest spec. ${Object.keys(out).length} ingredients.
 */

import type { Ingredient } from "@/types/ingredient";

export const INGREDIENTS: Record<string, Ingredient> = `;

fs.writeFileSync(
  new URL("src/data/ingredients/registry.generated.ts", base),
  header + JSON.stringify(out, null, 2) + ";\n",
);
console.log(
  `expanded ${Object.keys(out).length} ingredients to ${KEYS.length} nutrients each (${added} new cells filled)`,
);
